    private boolean isCheckAccount(String inputUsername, String inputPassword) {
        inputUsername = userJText.getText();
        inputPassword = passwordJPasswordFiled.getText();
        boolean flag = false;
        
        for (int i = 0; i < adminService.getAllAdmin().size(); i++) {
            Admin admin = adminService.getAllAdmin().get(i);
            if(inputUsername.equals(admin.getUserName()) 
               && inputPassword.equals(admin.getPassword())){
                 flag = true;
                 break;
            } 
        }
        return flag;
    }
    
    
