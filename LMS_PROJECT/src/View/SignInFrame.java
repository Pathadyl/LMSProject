    private void userJTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userJTextActionPerformed
        // TODO add your handling code here:
        String inputUsername = userJText.getText();
        String inputPassword = passwordJPasswordFiled.getText();
        if(inputPassword.isEmpty()) {
            warningLoginLabel.setText("Please enter password");
        } else if (!inputPassword.isEmpty()
                  && !isCheckAccount(inputUsername, inputPassword)) {
            warningLoginLabel.setText("username or password is incorrect!");
        } else if (isCheckAccount(inputUsername, inputPassword)) {
            Admin admin = new Admin();
            admin.setUserName(inputUsername);
            admin.setPassword(inputPassword);
            new ManageMentFrame(admin).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_userJTextActionPerformed

    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        // TODO add your handling code here:
        String inputUsername = userJText.getText();
        String inputPassword = passwordJPasswordFiled.getText();
        if(isCheckAccount(inputUsername, inputPassword)){
            Admin admin = new Admin();
            admin.setUserName(inputUsername);
            admin.setPassword(inputPassword);
            new ManageMentFrame(admin).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog( null, "Login Fail! Incorrect username or password" );
        }
    }//GEN-LAST:event_loginButtonActionPerformed

